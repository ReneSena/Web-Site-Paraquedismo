package modelo;


public class TipoAcesso {
    private int id;
    private String descricao;

    public TipoAcesso(){};
    
    public TipoAcesso(int idTipo){
        this.id = idTipo;
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
